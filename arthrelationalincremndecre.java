public class arthrelationalincremndecre
{
    //arithmetic,relational,increment and decrement
    public static void main(String args[])
    {
        int a=21;
        int b=32;
        System.out.println("value of a is greater than b "+(a>b));
        System.out.println("value of a is lesser than b "+(a<b));
        System.out.println("value of addition is "+(a+b));
        System.out.println("value of substraction is "+(a-b));
        System.out.println("value of multiplication is "+(a*b));
        System.out.println("value of divison is "+(a/b));
        System.out.println("value of percentage is "+(a%b));
        System.out.println("value of post-incrementation is "+(a++));
        System.out.println("value of pre-incrementation is "+(++a));
        System.out.println("value of pre-decrementation is  "+(--b));
        System.out.println("value of post-decrementation is "+(b--));
    }
}
